#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int i;

	i = 0;
	while (s[i] && s[i] != c)
		i++;
	if (s[i] == c || (c == '\0' && s[i] == c))
	{
		s = &s[i];
		return ((char *)s);
	}
	else
		return (NULL);
}
